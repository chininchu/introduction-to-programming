/*What is a Variable?
A variable in JavaScript is a container for storing data values.

Syntax: var variableName = value;

Oldest keyword for variable declaration.
Can be re-declared and updated (Not a good idea)


Some rules for naming variables:
Names must only contain letters, numbers or symbols $
First characters must not be a number
Names must not use reserved words such as var

*/

// Example:

var name = "Alice";
// var name = "Bob"; // Re-declaration
console.log(name); // Output: Bob


















