"use strict";
/*Accessing Array Elements

How-To: You can access any element in an array by using its index, with the first element at index 0.*/

const fruits = ['Apple', 'Banana', 'Cherry'];

console.log(fruits[0]); // Outputs: Apple

console.log(fruits[1]); // Outputs: Banana


/*Accessing elements by index is fundamental to working with arrays, whether it's reading a value or using it in a function.

*/


/*Changing an Array Element */


// Change the second fruit
fruits[1] = 'Blueberry';
console.log(fruits); // Outputs: ['Apple', 'Blueberry', 'Cherry']





